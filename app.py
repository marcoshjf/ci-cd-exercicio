def soma(a, b):
    """Retorna a soma de dois números (função atualizada)."""
    return a + b


def multiplica(a, b):
    """Retorna o produto de a e b."""
    return a * b


if __name__ == "__main__":
    # Exemplo rápido para executar localmente
    print("soma(2, 3) ->", soma(2, 3))
    print("multiplica(4, 5) ->", multiplica(4, 5))
